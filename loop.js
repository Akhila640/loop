// Example of For loop
for(var a=1;a<=5;a++){
    console.log(a)
}

for(var a=5;a>=1;a--){
    console.log(a)
}


for(var i=0;i<=5;i++){
}
console.log(i)

// Example of While loop
var a=1
while(a<=7){
    console.log(a)
    a++
}


var a=10
while(a>=5){
    console.log(a)
    a--
}


var i=1
while(i<4){
    i++
}
console.log(i)

